package week1.day1.assignments;

public class NegativeToPositive {
	
	
	public static void main(String[] args) {
		int number=-40;
		number=number*-1;
		if(number<0) {
			System.out.println("negative number");
		}
		if(number>0) {
			System.out.println("positive number");
		}
		System.out.println(number);
	}
	
}
